      <p>
        The AmazonFACE program is directed toward resolving a key source of uncertainty: the potential for rising atmospheric CO<sub>2</sub> concentrations -- which happens to be the proximate cause of climate change -- to buffer tropical forests against the deleterious effects of climate change by stimulating forest growth and resilience to drought. The existence, magnitude and duration of this supposed CO<sub>2</sub> fertilization effect in tropical forests remain largely undetermined, despite its potentially great importance for the global carbon cycle. A Free-Air CO<sub>2</sub> Enrichment (FACE) experiment is the most direct and robust scientific approach for reducing this uncertainty. No such experiment has ever been attempted in a tropical forest, despite the long- standing recognition in science and policy communities of the need for such an experiment.
